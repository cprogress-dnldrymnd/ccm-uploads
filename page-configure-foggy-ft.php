function ReplaceNumberWithCommas(total) {
//Seperates the components of the number
var n= total.toString().split(".");
//Comma-fies the first part
n[0] = n[0].replace(/\B(?=(\d{3})+(?!\d))/g, ",");
//Combines the two sections
return n.join(".");
}    


  

